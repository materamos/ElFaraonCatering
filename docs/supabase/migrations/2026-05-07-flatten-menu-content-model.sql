    when grill_item.item_id like '%choripan%' then 'choripan'
    when grill_item.item_id like '%hamburguesa%' then 'hamburguesa'
    when grill_item.item_id like '%bondiola%' then 'bondiola'
    when grill_item.item_id like '%matambre%' then 'matambre'
    when grill_item.item_id like '%entrana%' then 'entrana'
    when grill_item.item_id like '%lomo%' then 'lomo'
    when grill_item.item_id like '%bife-chorizo%' then 'bife-chorizo'
    else 'otros'
  end,
  grill_item.item_id,
  item.name,
  item.description,
  grill_item.note,
  item.image_path,
  grill_item.available,
  grill_item.pricing_key,
  grill_item.order_index
from menu_content.menu_grill_items grill_item
join menu_content.menu_items item
  on item.id = grill_item.item_row_id
 and item.item_id = grill_item.item_id
on conflict (item_id) do update
set
  family_id = excluded.family_id,
  name = excluded.name,
  description = excluded.description,
  note = excluded.note,
  image_path = excluded.image_path,
  available = excluded.available,
