begin;

do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conrelid = 'menu_content.menu_prices'::regclass
      and conname = 'menu_prices_kind_amount_valid'
  ) then
    alter table menu_content.menu_prices
      add constraint menu_prices_kind_amount_valid check (
        (kind = 'fixed' and amount is not null)
        or (kind in ('included', 'variants') and amount is null)
      );
  end if;

  if not exists (
    select 1
    from pg_constraint
    where conrelid = 'menu_content.menu_profile_facts'::regclass
      and conname = 'menu_profile_facts_link_pair_valid'
  ) then
    alter table menu_content.menu_profile_facts
      add constraint menu_profile_facts_link_pair_valid check (
        (link_text is null and link_href is null)
        or (link_text is not null and link_href is not null)
      );
  end if;
end $$;

create table if not exists menu_content.menu_daily_items (
  id bigint generated by default as identity primary key,
  item_id text not null unique check (item_id ~ '^[a-z0-9]+(?:-[a-z0-9]+)*$'),
  name text not null check (length(btrim(name)) > 0),
  description text null,
  note text null,
  available boolean not null default true,
  pricing_key text not null references menu_content.menu_prices(pricing_key),
  order_index integer not null unique check (order_index >= 0)
);

create table if not exists menu_content.menu_profile_service_settings (
  profile_id text primary key references menu_content.menu_profiles(id) on delete cascade,
  service_kind text not null check (service_kind in ('daily-menu', 'grill'))
);

create table if not exists menu_content.menu_catalog_sections (
  id bigint generated by default as identity primary key,
  section_id text not null unique check (section_id ~ '^[a-z0-9]+(?:-[a-z0-9]+)*$'),
