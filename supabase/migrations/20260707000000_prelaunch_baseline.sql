    return;
  end if;

  if target_name is null then
    return query select false, false, true, 'add_catalog_item_option', 'catalog_option_name_required';
    return;
  end if;

  if target_section_id !~ '^[a-z0-9]+(?:-[a-z0-9]+)*$'
