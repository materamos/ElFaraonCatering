CREATE OR REPLACE FUNCTION app_private.generate_admin_id(prefix text)
 RETURNS text
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'extensions', 'pg_temp'
AS $function$
declare
  clean_prefix text := nullif(btrim(generate_admin_id.prefix), '');
begin
  if clean_prefix is null or clean_prefix !~ '^[a-z0-9]+(?:-[a-z0-9]+)*$' then
    raise exception 'invalid admin id prefix';
  end if;

  return clean_prefix || '-' || left(replace(extensions.gen_random_uuid()::text, '-', ''), 12);
end;
$function$;

CREATE OR REPLACE FUNCTION app_private.menu_availability_target_exists(target_menu_id text, target_section_id text, target_item_id text)
 RETURNS boolean
 LANGUAGE sql
 STABLE SECURITY DEFINER
 SET search_path TO 'pg_temp'
AS $function$
  select exists (
    select 1
    from menu_content.menu_profiles profile
    join menu_content.menu_profile_service_settings settings
      on settings.profile_id = profile.id
    cross join menu_content.menu_daily_items item
    where profile.id = target_menu_id
      and target_section_id = 'menu-del-dia'
      and settings.service_kind = 'daily-menu'
      and item.item_id = target_item_id
  )
  or exists (
    select 1
    from menu_content.menu_profiles profile
    join menu_content.menu_profile_service_settings settings
      on settings.profile_id = profile.id
    cross join menu_content.menu_grill_catalog_items item
    where profile.id = target_menu_id
      and target_section_id = 'parrilla'
      and settings.service_kind = 'grill'
      and item.item_id = target_item_id
  )
  or exists (
    select 1
    from menu_content.menu_profiles profile
    cross join menu_content.menu_catalog_items item
    where profile.id = target_menu_id
      and item.section_id = target_section_id
      and item.item_id = target_item_id
  )
  or exists (
    select 1
    from menu_content.menu_profiles profile
    cross join menu_content.menu_catalog_items item
    join menu_content.menu_catalog_item_options option
      on option.catalog_item_id = item.id
    where profile.id = target_menu_id
      and item.section_id = target_section_id
      and item.item_id || '-' || option.option_id = target_item_id
  );
